public class Airplane
{
	private int x;
	private int y;
	private String name;
	
	public int getX()
	{
		return x;
	}
	
	public void setX(int i)
	{
		x = i;
		return;
	}
	
	public int getY()
	{
		return y;
	}
	
	public void setY(int i)
	{
		y = i;
		return;
	}
	public String getName()
	{
		return name;
	}
	
	public void setName(String i)
	{
		name = i;
		return;
	}
}
